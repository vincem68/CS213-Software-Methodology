package rupizza.rupizzeria;
/**
 * This enum is for the list of toppings that can be added to a Pizza.
 *
 * Hawaiian Pizza will always have Ham and Pineapple toppings.
 *
 * Pepperoni Pizza will always have a Pepperoni topping.
 *
 * Deluxe Pizza will always have Bacon, Chicken, Onion, Sausage, and
 * Mushroom toppings.
 *
 * @author Sawyer Reis, Vincent Mandola
 */
public enum Topping {
    pepperoni, pineapple, ham, bacon, onion, chicken, mushrooms,
    anchovies, sausage
}
